
//  In JavaScript, a higher-order function is one that either takes a function as an argument or returns a function as its result.
//  This concept allows for a more dynamic and flexible approach to working with functions, treating them as first-class citizens.
//  For instance, a higher-order function can accept a function parameter,
//  enabling operations like mapping over arrays with different transformations.
//  Alternatively, it can return a function, providing a way to create specialized functions based on parameters.
//  Higher-order functions are integral to functional programming,
//  promoting modularity and reusability in code by leveraging the versatility of functions as values in the language.